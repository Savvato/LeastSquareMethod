public class Point
{
    public double x1;
    public double x2;
    public double y;

    /**
     * Constructor
     *
     * @param x1 координата х1
     * @param x2 координата х2
     * @param y координата y
     */
    public Point(double x1, double x2, double y) {
        this.x1 = x1;
        this.x2 = x2;
        this.y = y;
    }
}
